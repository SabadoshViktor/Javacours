package ExtraHomeWork;

public class Box {
	
	int count;

	public Box(int count) {
		this.count = count;
	}

	@Override
	public String toString() {
		return "Box [count=" + count + "]";
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	

}
